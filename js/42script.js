let season = prompt("Enter season number");

if (season == "1") {
    alert("Your season is Spring.");
} else if (season == "2") {
    alert("Your season is Summer.");
} else if (season == "3") {
    alert("Your season is Autumn.");
} else if (season == "4") {
    alert("Your season is Winter.");
} else {
    alert("Unknown Season. Please enter a number in range from 1 to 4.");
}
